package fr.sihm.objects;

public class DemandeAcdc {

	private String id;
	private String status;
	private String type;
	private String origine;
	private String civitity;
	private String lastname;
	private String firstname;
	private String statusModif;
	
	public DemandeAcdc() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getOrigine() {
		return origine;
	}

	public void setOrigine(String origine) {
		this.origine = origine;
	}

	public String getCivitity() {
		return civitity;
	}

	public void setCivitity(String civitity) {
		this.civitity = civitity;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getStatusModif() {
		return statusModif;
	}

	public void setStatusModif(String statusModif) {
		this.statusModif = statusModif;
	}
	
	
}
